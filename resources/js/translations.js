export function useTranslations(locale) {
    const t = (key) => {
        const keys = key.split('.');
        let value = translations[locale] || translations['mk'];
        
        for (const k of keys) {
            value = value?.[k];
            if (value === undefined) break;
        }
        
        // Fallback to Macedonian if not found
        if (value === undefined) {
            value = translations['mk'];
            for (const k of keys) {
                value = value?.[k];
                if (value === undefined) break;
            }
        }
        
        return value || key;
    };
    
    return { t };
}
